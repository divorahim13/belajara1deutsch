import { createServerClient } from '@supabase/ssr'
import { cookies } from 'next/headers'

export async function createClient() {
  const cookieStore = await cookies()

  const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL
  const supabaseKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY

  if (!supabaseUrl || !supabaseKey || supabaseUrl.includes('REPLACE_WITH_YOUR_PROJECT_URL')) {
    // Return a proxy or a dummy client that doesn't throw on initialization
    // but might fail on actual calls (which we handle in components)
    // For now, we'll return a minimal dummy to satisfy the type
    return {
      auth: { getUser: () => Promise.resolve({ data: { user: null } }) },
      from: () => ({ select: () => ({ data: [], error: null }) }),
    } as any
  }

  return createServerClient(
    supabaseUrl,
    supabaseKey,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll()
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            )
          } catch {
            // The `setAll` method was called from a Server Component.
            // This can be ignored if you have middleware refreshing
            // user sessions.
          }
        },
      },
    }
  )
}
